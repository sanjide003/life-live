export type Language = 'en' | 'ml';

export function getTranslation(key: keyof typeof translations['en'], lang?: Language): string {
  const activeLang = lang || 'en';
  return translations[activeLang][key] || translations['en'][key] || key;
}

export function formatString(str: string, params: Record<string, string | number>): string {
  let result = str;
  Object.entries(params).forEach(([key, value]) => {
    result = result.replace(new RegExp(`{{${key}}}`, 'g'), String(value));
  });
  return result;
}
